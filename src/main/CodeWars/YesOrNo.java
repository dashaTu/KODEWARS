package CodeWars;//        Завершите  метод,
//        который принимает логическое значение и возвращает "Yes"
//        строку для true или "No" строку для false.

public class YesOrNo {
    public static String boolToWord(boolean b)
    {
        if (b == true){
            return "Yes";
        }
        else {}
        return "No";
    }


}
